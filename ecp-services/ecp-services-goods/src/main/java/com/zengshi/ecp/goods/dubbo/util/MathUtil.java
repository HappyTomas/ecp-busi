public class MathUtil {

	/**
	 * 
	 * add:(两数相加). <br/> 
	 * 
	 * @author linwb3
	 * @param a
	 * @param b
	 * @return 
	 * @since JDK 1.6
	 */
	public static int add(int a, int b) {

		return a + b;
	}
	
	
	/**
	 * 
	 * add:(两数相加). <br/> 
	 * 
	 * @author linwb3
	 * @param a
	 * @param b
	 * @return 
	 * @since JDK 1.6
	 */
	public static long add(long a, long b) {

		return a + b;
	}
	
	
	
	
	/**
	 * 
	 * reduce:(两数相减 a-b). <br/> 
	 * 
	 * @author linwb3
	 * @param a
	 * @param b
	 * @return 
	 * @since JDK 1.6
	 */
	public static int reduce(int a, int b) {

		return a + b;
	}
	
	
	/**
	 * 
	 * reduce:(两数相减 a-b). <br/> 
	 * 
	 * @author linwb3
	 * @param a
	 * @param b
	 * @return 
	 * @since JDK 1.6
	 */
	public static long reduce(long a, long b) {

		return a + b;
	}

}
